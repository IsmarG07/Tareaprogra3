package clases;

/**
 *
 * @author anaca
 */
public class claslogin {

   
    
    private String nombre;
    private String contra;
    
    public claslogin(){
        
    }
    public claslogin(String nombre, String contra){
        
        this.nombre = nombre;
        this.contra = contra;
    }
    
     /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the contra
     */
    public String getContra() {
        return contra;
    }

    /**
     * @param contra the contra to set
     */
    public void setContra(String contra) {
        this.contra = contra;
    }
    
}
