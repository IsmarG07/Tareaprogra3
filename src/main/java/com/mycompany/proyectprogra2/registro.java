    private void GUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GUARDARActionPerformed
        // TODO add your handling code here:
        
         login3 = new claslogin();
         
         if(usuario2.getText().equals("")){
            lanzarMensaje("Debe ingresar un nombre");
         } else if(password2.getText().equals("")){
             lanzarMensaje("Debe ingresar una contraseÃ±a");
         }else{
             
             login3.setNombre(usuario2.getText());
             login3.setContra(password2.getText());
             
             listalogin.add(login3);
             
             postgreeconexion conexion = new postgreeconexion();
            Connection CONEXION = conexion.connectarPostgree();
           
                 
             try {
                 
                  String SQL = "INSERT INTO public.login"
                 +"(nombre, contraseÃ±a)"
                 + "VALUES (?, ?)";
            
                 PreparedStatement statement;
                 statement = CONEXION.prepareStatement(SQL);
                 
                 statement.setString(1, login3.getNombre());
                 statement.setString(2, login3.getContra());
                 
                 int filasInsertadas = statement.executeUpdate(); 
                System.out.println("Insertado: " + filasInsertadas);
                
                CONEXION.close();//SIEMPRE SE CIERRA LA CONEXION
                CONEXION = null; // SE COLOCA A NULL LA CONEXION PARA QUE EL GC TOME ESTE OBJETO Y LO ELIMINE DE MEMORIA
                conexion = null;
                lanzarMensaje("Datos guardados con Ã©xito");
                
             } catch (SQLException ex) {
                 Logger.getLogger(registro.class.getName()).log(Level.SEVERE, null, ex);
                 lanzarMensaje("Error al grabar datos del paciente: " + ex.getMessage());
                 
             }
             
         login log = new login();
        log.setVisible(true);
        this.dispose();
         }
         
        
        
    }//GEN-LAST:event_GUARDARActionPerformed

